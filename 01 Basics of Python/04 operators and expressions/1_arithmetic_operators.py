# Basic math operations in Python

a = 10
b = 3

print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)       # float result
print("Floor Division:", a // b) # removes decimal
print("Modulus:", a % b)
print("Exponentiation:", a ** b)  # 10^3 = 1000