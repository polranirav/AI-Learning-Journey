import math

# Square root
print("Square root of 16:", math.sqrt(16))

# Power
print("2 to the power 3:", math.pow(2, 3))  # Returns float
print("2 ** 3:", 2 ** 3)  # Also works

# Logarithm
print("Log base e of 10:", math.log(10))
print("Log base 10 of 1000:", math.log10(1000))

# Rounding functions
print("Ceil of 4.3:", math.ceil(4.3))   # Rounds up
print("Floor of 4.7:", math.floor(4.7)) # Rounds down

# Factorial
print("Factorial of 5:", math.factorial(5))