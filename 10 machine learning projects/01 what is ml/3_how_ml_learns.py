# ML Workflow: How a machine actually learns

print("⚙️ Steps in a Machine Learning System:")

steps = [
    "1. Data Collection (features + labels)",
    "2. Model Selection (e.g., Linear Regression)",
    "3. Model Training (fit the model on training data)",
    "4. Model Evaluation (check performance)",
    "5. Prediction (use model on new data)"
]

for step in steps:
    print(step)

print("\n💡 Key Insight: The machine finds patterns in data instead of being told exact rules.")