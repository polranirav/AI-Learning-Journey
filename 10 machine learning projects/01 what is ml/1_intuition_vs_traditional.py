# Traditional Programming Approach
# You define rules, then give data → get output

# Rule: multiply by 2
def traditional_program(x):
    return x * 2

print("Traditional Programming Output:", traditional_program(5))  # Output: 10


# Machine Learning Approach
# You give input-output data → machine learns rules
print("\nMachine Learning Concept:")
print("We give data like:")
print("Input: [1, 2, 3] → Output: [2, 4, 6]")
print("The ML model figures out: 'multiply by 2' without you telling it.")