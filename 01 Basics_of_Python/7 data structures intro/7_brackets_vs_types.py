# Bracket types and their associated data structures

list_example = [1, 2, 3]         # square brackets = list
tuple_example = (1, 2, 3)        # parentheses = tuple
dict_example = {'a': 1, 'b': 2}  # curly braces = dictionary

print(type(list_example))
print(type(tuple_example))
print(type(dict_example))